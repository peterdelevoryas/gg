    const summary = model.ProblemSummary{
        .frontend_id = "1",
        .question_id = 1,
        .slug = "two-sum",
        .title = "Two Sum",
        .difficulty = .easy,
    };
    const detail = model.ProblemDetail{
        .frontend_id = "1",
        .question_id = 1,
        .slug = "two-sum",
        .title = "Two Sum",
        .code_snippets = &.{.{
            .lang = "Rust",
            .lang_slug = "rust",
            .code = "impl Solution { pub fn two_sum(nums: Vec<i32>, target: i32) -> Vec<i32> { vec![] } }",
        }},
    };

    const project = try ensureProblemProject(allocator, std.testing.io, root, summary, detail);
    const generated_solution = try std.Io.Dir.cwd().readFileAlloc(std.testing.io, project.solution_path, allocator, .limited(1024));
