    const lib_path = try config.pathJoin(allocator, &.{ project.dir, "src", "lib.rs" });
    const lib = try std.Io.Dir.cwd().readFileAlloc(std.testing.io, lib_path, allocator, .limited(1024));
    try std.testing.expect(std.mem.indexOf(u8, lib, "pub struct Solution;") != null);
    try std.testing.expect(std.mem.indexOf(u8, lib, "pub mod solution;") != null);
    try std.testing.expect(std.mem.indexOf(u8, lib, "include!(\"solution.rs\")") == null);

    try std.Io.Dir.cwd().writeFile(std.testing.io, .{ .sub_path = project.solution_path, .data = "custom\n" });
    _ = try ensureProblemProject(allocator, std.testing.io, root, summary, detail);
    const solution = try std.Io.Dir.cwd().readFileAlloc(std.testing.io, project.solution_path, allocator, .limited(1024));
    try std.testing.expectEqualStrings("custom\n", solution);

    const rust_project_path = try config.pathJoin(allocator, &.{ project.dir, "rust-project.json" });
    const rust_project = try std.Io.Dir.cwd().readFileAlloc(std.testing.io, rust_project_path, allocator, .limited(16 * 1024));
    try std.testing.expect(std.mem.indexOf(u8, rust_project, "\"root_module\": \"src/lib.rs\"") != null);
    try std.testing.expect(std.mem.indexOf(u8, rust_project, "\"name\": \"leetcode_support\"") != null);

    const manifest = try config.pathJoin(allocator, &.{ root, "Cargo.toml" });
    const result = std.process.run(allocator, std.testing.io, .{
        .argv = &.{ "cargo", "metadata", "--manifest-path", manifest, "--format-version", "1", "--no-deps" },
        .stdout_limit = .limited(1024 * 1024),
        .stderr_limit = .limited(1024 * 1024),
    }) catch |err| switch (err) {
        error.FileNotFound => return error.SkipZigTest,
        else => |e| return e,
    };
    try std.testing.expectEqual(std.process.Child.Term{ .exited = 0 }, result.term);
}

